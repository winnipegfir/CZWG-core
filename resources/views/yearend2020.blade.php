    <h1 class="font-weight-bold blue-text">Nate's 2020 Letter to the FIR</h1>
        <p>Friends,</p>
            <p>As 2020 comes to a close, it's a good time to take a minute to look back at the past year for our FIR - and what a year it was.</p>
        <hr>
        <h5 class="blue-text font-weight-bold">To my Staff,</h5>
        <br>
            <p>Before the year went from normal to what we now know 2020 to be, I received an email from a VATCAN student - he was an S3 at the
            time. And I'll be honest, I didn't know who he was at all when I first got the email. I had to check to make sure he was actually a
            member of the VATSIM network. Regardless, his email was very well-written. He started his message:</p>
            <blockquote style="font-size: 1em">"Nate, let's be honest. Winnipeg needs a Deputy Chief - you can't do this all by yourself."</blockquote>
            <p>While the FIR was still quiet, and I was essentially managing the whole FIR as a one-man team, I knew this random student was right.
            His email was blunt - but he was right. And, not surprisingly, his email was to express his interest in transfering to this small FIR,
            and take the position.</p>
            <p>We had a long chat about the job - that it wouldn't be something you could just take up and leave if you got bored. The various tasks
            of a Deputy also meant that we would need to have a good working relationship - and thankfully, nearly a year later, Kolby Dunning has
            remained in his position the whole time ever since.</p>
            <p>Shortly after Kolby's appointment, Winnipeg hired an Events Coordinator in February, a Chief Instructor in March, and a Webmaster 
            and Facility Engineer in May. The FIR, for the first time in a long time, had a full staff.</p>
            <p>While Luke Wightman departed his Events Coordinator position earlier this year, our team of Kolby and myself, as well as Dario Marnika, 
            James White and Ethan Mason have kept Winnipeg moving forward. And to all of you, I want to thank you. Your work to keep the FIR going
            is often unnoticed by many as so much work is done in the background - but it is all appreciated. Your work has taken us from Canada's 
            loneliest and smallest FIR to one that is growing fast, and has some of the best staff VATSIM has to offer.</p>
        <hr>
        <h5 class="blue-text font-weight-bold">To the Winnipeg Controllers,</h5>
        <br>
            <p>When I took the role of Winnipeg Chief over a year ago, I had no idea how to even begin staffing up. We were surrounded by neighbouring 
            FIRs and ARTCCs with far more traffic, bigger airports and more staff than we had - and what does a small FIR in the middle of Canada have 
            to offer compared to that?</p>
            <p>Turns out, we have a lot to offer - and you all have led me to realize that over the past year. From that one span of a few weeks in early
            2020, just before lockdowns across the world kept us all at home, Winnipeg went for the Iron Mic award - given to the FIR/ARTCC/vACC with the 
            most hours online every week - and despite our small roster, we had a week where we came in the top 15 in EVERY position on the network, and
            2nd place on Centre across VATSIM. It's amazing what can happen when you work as a team - and that Iron Mic run was just the start.</p>
            <p>Throughout 2020, our roster has steadily continued growing - our Instructing team has gone from only a couple to 7. We have Mentors always 
            helping members of the FIR as well - and the sense of community this FIR has is something I've always adored. I'm extremely thankful we 
            continue to have that to this day, and look forward to keeping that as we move forward into the new year.</p>
            <p>As controllers in Winnipeg - each of you have proven that just because you may not have the biggest airports, the craziest events, or the largest airspace,
            that doesn't mean you can't create a strong family of members. And as we keep growing, you all deserve my thanks for keeping this FIR active - 
            Winnipeg has some of the most hours controlled on VATSIM this year - and considering our competition for the top spot, even being near the top 
            is something to be extremely proud of. Give yourselves a pat on the back - but don't stop now.</p>
        <hr>
        <h5 class="blue-text font-weight-bold">To the VATCAN Staff,</h5>
        <br>
            <p>It was just under two years ago that I was in charge of this entire division - and being VATCAN1 really showed me the power this country has on
            the VATSIM network - we may not be nearly as busy as our friends south of the border in VATUSA, but Canada has always had strong numbers
            on this network.</p>
            <p>Since leaving my position as a Division Staff member for the role I've had ever since, Winnipeg and I been lucky enough to have the continued
            support of my succesors in the VATCAN Executive Team. While my departure from VATCAN1 wasn't as pretty as I wish it could have been, I've been
            constantly reminded that the move to the position of Winnipeg Chief was the right choice - and your help in making Winnipeg the FIR it is today
            is greatly appreciated.</p>
            <p>The relationship that Winnipeg has with the VATCAN Staff has always been strong - and I look forward to continuing that relationship as we
            move into 2021.</p>
            <hr>
        <h5 class="blue-text font-weight-bold">On a Personal Note,</h5>
        <br>
            <p>This year was truly something interesting. I, like many, lost my job in the aviation earlier this year, when the COVID-19 pandemic was just
            beginning to impact Canada. I haven't been to work since - but hope that will soon change. However, the time off gave me a lot of free time - 
            as with many members of the VATSIM network. And between a global pandemic and the release of new Flight Simulators including Microsoft's highly anticipated 
            Flight Simulator 2020, our network has never seen so much traffic.</p>
            <p>As the network continues to grow as we pass into 2021, and as the world hopefully slowly moves back to normal, I want to thank everyone
            that has helped Winnipeg grow this past year. Whether you're a neighbouring controller who's come online to help push traffic to fly Winnipeg,
            or if you're one of Winnipeg's many pilots we see frequently on the network - from the bottom of my heart, thank you.</p>
